# casting from one datatype to other datatype and also we can change datatype

#int()-change to integer
#float()-change to float
#str()-change to string

#example for casting to String

x=int(12.12);   #int
y=int("12");    #int
z=int(23);      #int

print(type(x)); 
print(type(y));
print(type(z));

#example for casting to float

a=float(12.12); #float
b=float("12");  #float
c=float(23);    #float

print(type(a));
print(type(b));
print(type(c));

#example for casting to String

n=str(12.12); #str
m=str("12");  #str
p=str(23);    #str

print(type(n));
print(type(m));
print(type(p));


# to display number from caracheter or character to number
print(chr(53));
print(ord('E'));
